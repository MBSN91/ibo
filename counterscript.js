var waypoint = new Waypoint({
    element: document.getElementById('stoffen'),
    handler: function(direction) {
      const options = {
    duration: 5,
  };
  let demo = new CountUp('stoffen', 7068, options);
  if (!demo.error) {
    demo.start();
  } else {
    console.error(demo.error);
  }
    },
    offset: '100%'
  })

